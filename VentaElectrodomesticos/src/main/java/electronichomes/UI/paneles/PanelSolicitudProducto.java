    public void inicializarComboBox() {
        for (int i = 0; i < listaEstablecimientos.size(); i++) {
            this.jComboBoxEstablecimientos.addItem(listaEstablecimientos.get(i).getNombre());
        }
    }
    
    public Existencia getExistenciaSolicitada() {
        try {
            String codigoProducto = jTextFieldCodigo.getText();
            Producto producto = Producto.getProductoPorCodigo(codigoProducto);
            int cantidad = Integer.parseInt(jTextFieldCantidad.getText());
            int index = jComboBoxEstablecimientos.getSelectedIndex();
            Establecimiento establecimiento = this.listaEstablecimientos.get(index);
            
            return new Existencia(producto, establecimiento, cantidad);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocurrio un error: " + e.getMessage());
            return null;
        }
    }

