    private void jButtonIngresarNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIngresarNuevoActionPerformed
        PanelDatosProducto panel = new PanelDatosProducto();
        panel.limpiarCampos();
        int boton = JOptionPane.showOptionDialog(null, panel, "Ingresar producto", 2, JOptionPane.PLAIN_MESSAGE, null, new String[]{"Guardar","Cancelar"}, null);

        if (boton==0) {
            Producto nuevoProducto = panel.getNuevoProducto();
            
            if (nuevoProducto==null) {
                JOptionPane.showMessageDialog(null, "No se puedo ingresar el producto","ERROR",JOptionPane.ERROR_MESSAGE);
            } else {
                String cantidadtxt = JOptionPane.showInputDialog(null, "Ingrese la cantidad de unidades que hay del producto");
                int cantidad = Integer.parseInt(cantidadtxt);
            
                if (cantidad>0) {
                    this.empleadoInventario.setEstablecimiento(establecimientoFijo);
                    Establecimiento establecimiento = this.empleadoInventario.getEstablecimiento();
                    Existencia existencia = new Existencia(nuevoProducto,establecimiento,cantidad);

                    nuevoProducto.ejecutarInstruccionPSQL(Producto.INSERT);
                    existencia.ejecutarInstruccionPSQL(Existencia.INSERT);
                    this.actualizarTabla();
                } else {
                    JOptionPane.showMessageDialog(null, "El valor ingresado no es correcto");
                }
            }
        }
    }//GEN-LAST:event_jButtonIngresarNuevoActionPerformed



