    private void jButtonSolicitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSolicitarActionPerformed

        PanelSolicitudProducto panel = new PanelSolicitudProducto();
        int boton = JOptionPane.showOptionDialog(null, panel, "Solicitar producto", 2, JOptionPane.PLAIN_MESSAGE, null, new String[]{"Guardar","Cancelar"}, null);
        
        if (boton==0) {
            Existencia eSolicitada = panel.getExistenciaSolicitada();
            if (eSolicitada==null) {
                JOptionPane.showMessageDialog(null, "No se pudo hacer la solicitud");
            } else {
                int cantidad = eSolicitada.getCantidad();
                String codigoProducto = eSolicitada.getProducto().getCodigo();
                Existencia eExterior = Existencia.getExistenciaPorIDS(codigoProducto, eSolicitada.getEstablecimiento().getId());
                eExterior.setCantidad(eExterior.getCantidad()-cantidad);
                eExterior.ejecutarInstruccionPSQL(Existencia.UPDATE);

                Existencia eInterna = Existencia.getExistenciaPorIDS(codigoProducto, establecimientoFijo.getId());
                
                if (eInterna==null) {
                    Producto producto = Producto.getProductoPorCodigo(codigoProducto);
                    eInterna = new Existencia(producto,establecimientoFijo,cantidad);
                    eInterna.ejecutarInstruccionPSQL(Existencia.INSERT);
                } else {
                    eInterna.setCantidad(eInterna.getCantidad()+cantidad);
                    eInterna.ejecutarInstruccionPSQL(Existencia.UPDATE);
                }
                JOptionPane.showMessageDialog(null, "Solicitud exitosa!");
                actualizarTabla();
            }
        }
        
        

    }//GEN-LAST:event_jButtonSolicitarActionPerformed

